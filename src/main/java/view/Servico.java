    private void jTable4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable4MouseClicked
        row = jTable4.rowAtPoint(evt.getPoint());
        int id = (int)mServ.getValueAt(row, 0);
        
        sNome.setText((String)ser.get(id).getNome());
        equip.setText((String)ser.get(id).getEquipamentos());
        
        select = true;
            change();
        mSelec.setRowCount(0);
        for(PROFISSIONAL a : ser.get(id).getProfissionais()){
            mSelec.insertRow(mSelec.getRowCount(), new Object[]{Integer.toString((int)a.getId()),a.getName()});
        }
        
    }//GEN-LAST:event_jTable4MouseClicked
    
    public void updateProf(){
        modelo.setRowCount(0);
        for(PROFISSIONAL pro : ser.allProf().values()){
            modelo.insertRow(modelo.getRowCount(), new Object[]{(int)pro.getId(),(String)pro.getName(),pro.getEspecialidade()});
        }
    }
    
    private void jButton7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton7MouseClicked
        try{
            if(!equip.getText().equals("") && mSelec.getRowCount() > 0){
                if(select){
                    int id = (int)modelo.getValueAt(row, 0);
                    mServ.setValueAt((String)sNome.getText(), row, 1);
                    mServ.setValueAt((String)equip.getText(), row, 2);
                    ser.add(id,new SERVICO(id,(String)sNome.getText(),ProfServ(),(String)equip.getText()));
                    limparServ();
                }else{
                    ser.add(contador, new SERVICO(contador,(String)sNome.getText(),ProfServ(),(String)equip.getText()));
                    mServ.insertRow(mServ.getRowCount(), new Object[]{contador,(String)sNome.getText(),(String)equip.getText()});
                    contador++;
                    limparServ();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Preencha os campos corretamente!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar!");
        }
    }//GEN-LAST:event_jButton7MouseClicked

