    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        int newRow = jTable2.rowAtPoint(evt.getPoint());
        int id = (int)modelo.getValueAt(newRow, 0);
        boolean has = false;
        for (int i = 0; i < mSelec.getRowCount(); i++) {
            if(Integer.parseInt((String)mSelec.getValueAt(i, 0)) == id){
               has = true; 
            }
        }
        if(!has)
            mSelec.insertRow(mSelec.getRowCount(), new Object[]{Integer.toString((int)ser.getProf(id).getId()),(String)ser.getProf(id).getName()});
    }//GEN-LAST:event_jTable2MouseClicked

    PROFISSIONAL[] ProfServ(){
        PROFISSIONAL[] newProff = new PROFISSIONAL[mSelec.getRowCount()];
                
        for (int i = 0; i < mSelec.getRowCount(); i++) {
            newProff[i] = ser.getProf(Integer.parseInt((String)mSelec.getValueAt(i, 0)));
        }
        return newProff;
    }
    
void limparServ(){
        sNome.setText("");
        equip.setText("");
        mSelec.setRowCount(0);
        select = false;
        make();
    }
