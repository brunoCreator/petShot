    PROFISSIONAL[] ProfServ(){
        PROFISSIONAL[] newProff = new PROFISSIONAL[mSelec.getRowCount()];
                
        for (int i = 0; i < mSelec.getRowCount(); i++) {
            newProff[i] = ser.getProf((int)mSelec.getValueAt(i, 0));
        }
        return newProff;
    }
    
    void limpar(){
        make();
        mSelec.setRowCount(0);
        select = false;
    }
    
