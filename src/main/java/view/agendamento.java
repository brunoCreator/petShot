    int contador = 0;
    int row = 0;
    boolean select = false;

    public void update() {
        jComboBox1.removeAllItems();
        for (CLIENTE s : cad.getAll().values()) {
            jComboBox1.addItem(new Box((int) s.getId(), s.getNome()));
        }

        jComboBox3.removeAllItems();
        for (SERVICO s : ser.getAllServ().values()) {
            jComboBox3.addItem(new Box((int) s.getId(), s.getNome()));
        }
    }


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (jComboBox1.getItemCount() != 0 && jComboBox2.getItemCount() != 0 && jComboBox3.getItemCount() != 0) {
                if (select) {
                    Box cli = (Box) jComboBox1.getSelectedItem();
                    Box ani = (Box) jComboBox2.getSelectedItem();
                    Box serv = (Box) jComboBox3.getSelectedItem();
                    int id = (int) modelo.getValueAt(row, 0);

                    age.addMento(id, new AGENDAMENTO(id,
                            cad.getAni(ani.getKey()),
                            ProfServ(),
                            cad.get(cli.getKey()),
                            ser.get(serv.getKey())));

                    AGENDAMENTO ag = age.getMento(id);

                    modelo.setValueAt((String) ag.getCliente().getNome(), row, 1);
                    modelo.setValueAt((String) ag.getAnimal().getNome(), row, 2);
                    modelo.setValueAt((String) ag.getServico().getNome(), row, 3);

                    limpar();
                } else {
                    Box cli = (Box) jComboBox1.getSelectedItem();
                    Box ani = (Box) jComboBox2.getSelectedItem();
                    Box serv = (Box) jComboBox3.getSelectedItem();

                    age.addMento(contador, new AGENDAMENTO(contador,
                            cad.getAni(ani.getKey()),
                            ProfServ(),
                            cad.get(cli.getKey()),
                            ser.get(serv.getKey())));

                    AGENDAMENTO novo = age.getMento(contador);
                    modelo.insertRow(modelo.getRowCount(), new Object[]{contador,
                        (String) novo.getCliente().getNome(),
                        (String) novo.getAnimal().getNome(),
                        (String) novo.getServico().getNome()});
                    contador++;
                    limpar();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Preencha os campos corretamente!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar!");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        row = jTable1.rowAtPoint(evt.getPoint());
        int id = (int) modelo.getValueAt(row, 0);
        AGENDAMENTO ag = age.getMento(id);
        jComboBox1.getModel().setSelectedItem(new Box((int) ag.getCliente().getId(), ag.getCliente().getNome()));
        jComboBox2.getModel().setSelectedItem(new Box((int) ag.getAnimal().getId(), ag.getAnimal().getNome()));
        jComboBox3.getModel().setSelectedItem(new Box((int) ag.getServico().getId(), ag.getServico().getNome()));
        
        mSelec.setRowCount(0);
        for(PROFISSIONAL pro : ag.getProfissionais()){
            mSelec.insertRow(mSelec.getRowCount(), new Object[]{(int)pro.getId(),(String)pro.getName(),pro.getEspecialidade()});
        }
        
        select = true;
        change();
    }//GEN-LAST:event_jTable1MouseClicked

    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        if (jComboBox1.getItemCount() > 0) {
            Box item = (Box) jComboBox1.getSelectedItem();

            jComboBox2.removeAllItems();
            for (ANIMAL s : cad.get((int) item.getKey()).getAnimais()) {
                jComboBox2.addItem(new Box((int) s.getId(), s.getNome()));
            }
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (select) {
            age.removeMento((Integer) modelo.getValueAt(row, 0));
            modelo.removeRow(row);
            limpar();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

