    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        int newRow = jTable2.rowAtPoint(evt.getPoint());
        int id = (int)proff.getValueAt(newRow, 0);
        boolean has = false;
        for (int i = 0; i < mSelec.getRowCount(); i++) {
            if((int)mSelec.getValueAt(i, 0) == id){
                has = true;
            }
        }
        if(!has)
        mSelec.insertRow(mSelec.getRowCount(), new Object[]{(int)ser.getProf(id).getId(),(String)ser.getProf(id).getName()});
    }//GEN-LAST:event_jTable2MouseClicked

    private void jComboBox3ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox3ItemStateChanged
        if(jComboBox3.getItemCount() > 0){
        proff.setRowCount(0);
        mSelec.setRowCount(0);
        Box serv = (Box) jComboBox3.getSelectedItem();
        for(PROFISSIONAL pro : ser.get(serv.getKey()).getProfissionais()){
            proff.insertRow(proff.getRowCount(), new Object[]{(int)pro.getId(),(String)pro.getName(),pro.getEspecialidade()});
        }}
    }//GEN-LAST:event_jComboBox3ItemStateChanged

